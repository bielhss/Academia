     private void PreencheOsCampos() {

        AlunosDAO vdao = new AlunosDAO();

        for (Alunos v : vdao.FindAll()) {

            cmbNome.addItem(v);
        }

        PersonalDAO cdao = new PersonalDAO();

        for (Personal c : cdao.FindAll()) {

            cmbCref.addItem(c);
        }
    }
     
     private void PreencheCBTable(Alunos idAlunos, Personal idPersonal) {
        cmbCref.setEditable(true);
        cmbNome.setEditable(true);
        cmbCref.setEditable(false);
        cmbNome.setEditable(false);
        txtNomePersonal.setText(idPersonal.getNome());
        txtNumero.setText(idAlunos.getNumero());
       
    }
     
     
     

