    private void aula() {
        Personal personal =(Personal) cmbCref.getSelectedItem();
        Alunos alunos = (Alunos) cmbNome.getSelectedItem();

        AtividadeDAO dao = new AtividadeDAO();
        Atividade atividade = new Atividade();

        atividade.setAlunos(alunos);
        atividade.setPersonal(personal);
        

        dao.save(atividade);
    }
    
     public void readJTable() {

        DefaultTableModel modelo = (DefaultTableModel) jTableAtividade.getModel();
        modelo.setNumRows(0);
        AtividadeDAO vdao = new AtividadeDAO();
        for (Atividade v : vdao.FindAll()) {
            modelo.addRow(new Object[]{
                v.getIdatividade(),
                v.getPersonal(),
                v.getAlunos(),
            });

        }
    }
     
