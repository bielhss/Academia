    private void cadastraaluno() {
        Alunos v = new Alunos();
        AlunosDAO vdao = new AlunosDAO();
        v.setNome(txtNome.getText());
        v.setSexo(txtSexo.getText());
        v.setAltura(txtAltura.getText());
        v.setIdade(txtIdade.getText());
        v.setEndereco(txtEndereco.getText());
        v.setNumero(txtNumero.getText());
        v.setBairro(txtBairro.getText());
        v.setCidade(txtCidade.getText());
        v.setTelefone(txtTelefone.getText());
        v.setCep(txtCep.getText());
        v.setPeriodo(cmbPeriodo.getSelectedItem().toString());
        v.setAtividades(cmbAtividades.getSelectedItem().toString());
        
        vdao.save(v);
        limpa();
        readJTable();
    }
    
    private void excluialuno(){
        AlunosDAO vdao = new AlunosDAO();
        vdao.remove((int) jTableAlunos.getValueAt(jTableAlunos.getSelectedRow(), 0));
        limpa();
        readJTable();
    }

private void atualizaaluno() {
        Alunos v = new Alunos();
        AlunosDAO vdao = new AlunosDAO();
        v.setIdAluno((int) jTableAlunos.getValueAt(jTableAlunos.getSelectedRow(), 0));
        v.setNome(txtNome.getText());
        v.setSexo(txtSexo.getText());
        v.setAltura(txtAltura.getText());
        v.setIdade(txtIdade.getText());
        v.setEndereco(txtEndereco.getText());
        v.setNumero(txtNumero.getText());
        v.setBairro(txtBairro.getText());
        v.setCidade(txtCidade.getText());
        v.setTelefone(txtTelefone.getText());
        v.setCep(txtCep.getText());
        v.setPeriodo(cmbPeriodo.getSelectedItem().toString());
        v.setAtividades(cmbAtividades.getSelectedItem().toString());
        
        vdao.update(v);
        limpa();
        readJTable();
    }

