    public void readJTable() {

        DefaultTableModel modelo = (DefaultTableModel) jTableAlunos.getModel();
        modelo.setNumRows(0);
        AlunosDAO vdao = new AlunosDAO();
        vdao.FindAll().forEach((Alunos v) -> {
            modelo.addRow(new Object[]{
                v.getIdAluno(),
                v.getNome(),
                v.getIdade(),
                v.getAtividades(),
                v.getTelefone(),
            });
        });
    }

